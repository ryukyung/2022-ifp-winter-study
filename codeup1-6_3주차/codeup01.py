# 기초- 출력
# [1] Hello 출력
print('Hello')

# [2] Hello World 출력
print('Hello World')

# [3] Hello
#     World 출력 (두 줄에 걸쳐 줄을 바꿔 출력하기)
print('''Hello
World''')
print('Hello\nWorld')

# [4] 'Hello' 출력 (단, 작은 따옴표도 함께 출력)
print("""'Hello'""")

# [5] "Hello World" 출력 (단, 큰 따옴표도 함께 출력)
print('''"Hello World"''')

# [6] "!@#$%^&*()" 출력 (단, 큰 따옴표도 함께 출력)
print('''"!@#$%^&*()"''')

# [7] "C:\Download\hello.cpp" 출력 (단, 큰 따옴표도 함께 출력)
print('''"C:\Download\hello.cpp"''')

# [8] ┌┬┐ 출력
#     ├┼┤
#     └┴┘ 
print("""\
┌┬┐
├┼┤
└┴┘""")